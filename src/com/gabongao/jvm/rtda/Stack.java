public class Stack {
    int maxSize, size = 0;
    Frame _top;

    public Stack(int maxSize) {
        this.maxSize = maxSize;
    }

    public void push(Frame frame) {
        if (size >= maxSize) {
            throw new RuntimeException("StackOverFlowError");
        }
        if (_top != null) {
            frame.setLower(_top);
        }
        _top = frame;
        size++;
    }

    public Frame pop() {
        if (_top == null) {
            throw new RuntimeException("jvm stack is empty");
        }
        Frame top = _top;
        _top = top.getLower();
        top.setLower(null);
        size--;
        return top;
    }

    public Frame top() {
        if (_top == null) {
            throw new RuntimeException("jvm stack is empty");
        }
        return _top;
    }
}
